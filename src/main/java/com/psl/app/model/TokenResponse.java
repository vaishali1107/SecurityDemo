package com.psl.app.model;

public class TokenResponse {

	String token;

	
	public TokenResponse() {
		super();
	}

	public TokenResponse(String token) {
		super();
		this.token = token;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
	
	
}
